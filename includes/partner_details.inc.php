    var eCurrentImage = null;
    function syncPix(e, pseudoSiliderId){
        if ($(window).width() <= 414) {
            return;
        }
        if (eCurrentImage != null) {
            eCurrentImage.removeClass('mirror');
        }
        var eImage = $(e.target);
        $(pseudoSiliderId).fadeOut('fast', function() {
            $(pseudoSiliderId).attr('src', eImage.attr('src'));
            $(pseudoSiliderId).fadeIn('fast');
        });

        eImage.addClass('mirror');
        eCurrentImage = eImage;
    }
    // Custom three-pic slider
    $('#partner_car').carousel({
        interval: false
    });
    $('#partner_car_2').carousel({
        interval: false
    });

    $('#partner_car .item').each(function(){
        var next = $(this).next();

        var eNext;
        for (var i=0;i<2;i++) {
            if (!next.length) {
                next = $(this).siblings(':first');
            }
            eNext = next.children().first();
            eNext.on('click',function(e){
                syncPix(e, '#pseudoslider');
            }).clone(true).appendTo($(this));
            next=next.next();
        }
    });
